async def seed_pages():
    async with AsyncSessionLocal() as db:
        for slug, title, group, position, content in PAGES:
            # Не перезаписываем, если страница уже есть (чтобы не затереть правки)
            existing = await db.execute(select(InfoPage).where(InfoPage.slug == slug))
            if existing.scalar_one_or_none():
                continue
            db.add(
                InfoPage(
                    slug=slug,
                    title=title,
                    footer_group=group,
                    position=position,
                    content=content.strip(),
                    is_published=True,
                )
            )
        await db.commit()
        print("Информационные страницы созданы.")


if __name__ == "__main__":
    asyncio.run(seed_pages())
